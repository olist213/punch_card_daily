# int to float

num_int = 10
print('num_int',num_int) # 10
num_float = float(num_int) 
print(num_float) # 10.0

# float to int

num_float1 = 9.18
print(int(num_float1))

# int to float
num_int = 10
print('num_int',num_int) # 10
num_str = str(num_int)
print(num_str)

# str to int or float

num_str = '10'
print('num_int',int(num_str))
print('num_int',float(num_str))

# str to list

string1 = 'hello,world'
