# check data types

#int
print(type(10))

# float
print(type(3.14))

#complex
print(type(1 + 3j))

#str
print(type("my name is liangcheng"))

#list
print(type([1,2,3]))

#dict
print(type({'name':'liangcheng'}))

#set
print(type({8.7,9.0,23.89}))

#tuple
print(type((8.7,9.0,23.89)))
